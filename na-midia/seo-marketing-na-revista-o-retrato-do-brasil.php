                  <p>No momento, há um grande esforço por parte de agentes de publicidade e também de anunciantes para entenderem as mídias sociais e se adequarem rapidamente à nova estratégia publicitária. "A princípio, tudo é muito simples: o objetivo dos anunciantes é que os cliques que os usuários dão nos anúncios se revertam em vendas, ou que, pelo menos, sua marca passe a ser um hit na rede", dis Bernhard Schultze, publicitário que concebeu sua agência SEO Marketing já sob os novos conceitos da mídia social digital, sendo especialista em otimização de mecanismos de pesquisa (SEO, na sigla em inglês), conjunto de práticas para aumentar as chances dos anunciantes de se destacar nos sites de busca, como o Google. "Já para os sites sociais, o objetivo é que os usuários simplesmente cliquem nos anúncios em suas páginas, porque, na maioria das vezes, os anunciantes pagam por clique que recebem."</p>

