    	<h1><small>Tutorial SEO</small><br/>Black Hat SEO e White Hat SEO</h1>
        <h2>Saiba quais são as implicações destes diferentes tipos de técnicas</h2>
        
    	<p>Existem duas vertentes da <strong>Otimização de Sites (Search Engine Optimization)</strong>. Uma é chamada de <strong>White Hat SEO</strong> (chapéus brancos) e a outra de <strong>Black Hat SEO</strong> (chapéus pretos). Não é difícil de adivinhar que uma segue as diretrizes do Google e demais buscadores, enquanto a outra procura brechas nos algoritmos dos mecanismos de busca para melhorar o seu posicionamento nas <strong>páginas de resultado (SERP)</strong> ao utilizar técnicas "sujas".</p>
        
    	<h2>Black Hat SEO (Chapéus Pretos)</h2>
        
    	<p>Black Hat é um termo utilizado na informática para se referir as pessoas ou técnicas que visam atingir um objetivo sem a autorização do órgão, empresa ou pessoa responsável. Esse objetivo pode ser a entrada em um sistema protegido, monetização por meios não autorizados ou o acesso às informações confidenciais.</p>
        
    	<p>O Google procura sempre oferecer aos usuários o resultado mais relevante à busca por ele efetuada. Os sites relevantes aparecem mais bem posicionados. Estima-se que o Google utilize em torno de 200 variáveis para definir o posicionamento de um site para cada palavra-chave. No mundo SEO, Black Hat se refere a um conjunto de técnicas que têm o objetivo de manipular as principais variáveis para enganar os mecanismos de busca, oferecendo-lhes, geralmente, conteúdo diferente do proposto.</p>
        
    	<p>Extraído das <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=35769">diretrizes para webmasters</a> do Google, temos o seguinte texto, que descreve algumas técnicas comumente usadas em sites Black Hat SEO:</p>
        
    	<ul>
    	  <li>Evite <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=66353">usar texto ou links ocultos</a>.</li>
    	  <li>Não utilize <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=66355">técnicas de cloaking (camuflagem do conteúdo real da página) nem faça redirecionamentos não-autorizados</a>.</li>
    	  <li>Não <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=66358">carregue páginas com palavras-chave irrelevantes.</a></li>
    	  <li>Não crie múltiplas páginas, subdomínios ou domínios com <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=66359">conteúdo muito repetido</a>.</li>
    	  <li>Evite <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=66355">usar páginas de &quot;entrada&quot; criadas apenas em função dos mecanismos de pesquisa</a> ou outras técnicas &quot;pré-fabricadas&quot;, como programas de filiação, que têm <a href="http://www.google.com/support/webmasters/bin/answer.py?answer=66361">pouco ou nenhum conteúdo original</a>.</li>
  	    </ul>
      
    	<h2>Como o Google reage aos sites com Black Hat?</h2>
        
    	<p>O Google afirma identificar a grande parte de truques para manipular os seus resultados. De fato, cada vez mais o Googlebot procura "emular" o comportamento das pessoas e, entre os principais mecanismos de pesquisa, é de longe o que melhor realiza essa tarefa. Além disso, o Google oferece um endereço para que webmasters denunciem sites que possam ser considerados spam ou que tenham inflado artificialmente os seus posicionamentos para determinadas palavras-chave. É preciso ter pelo menos um site cadastrado no Google Webmaster Tools para poder acessar essa ferramenta: <a href="https://www.google.com/webmasters/tools/spamreport?hl=pt_br">https://www.google.com/webmasters/tools/spamreport?hl=pt_br</a>.</p>
        
    	<p>Os mecanismos de busca podem tomar diversas ações, dentre elas:</p>
        
    	<ul>
    	  <li>Desconsiderar de seu algoritmo o que considera irregular, sem penalizar o site</li>
    	  <li>Penalizar o site com a perda de posições na tela de resultados</li>
    	  <li>Desindexar (apagar) o site de seu índice</li>
  	    </ul>
      
    	<p>O fato é que, infelizmente, nem todos os sites são detectados ou penalizados pelos buscadores. A SEO Marketing criou uma área em seu site para <a href="http://seomarketing.com.br/contato-bh.html">analisar sites que utilizem técnicas Black Hat</a> e que estejam bem posicionadas nos buscadores, para monitorar o tempo que leva para que detectem as irregulariedades e tomem uma ação em cima.</p>
        
    	<h2>Ferramentas para detectar sites com Black Hat SEO</h2>
        
    	<p>Dois programas (em inglês) podem ajudá-lo a detectar sites que utilizam técnicas Black Hat SEO:</p>
        
    	<ul>
    	  <li><a href="http://tool.motoricerca.info/spam-detector/">Search Engine Spam Detector</a>: identifica texto invisível, texto que foge de padrões naturais de linguagem, palavras-chave demasiadamente enfatisadas, ou doorway pages.</li>
    	  <li><a href="http://www.copyscape.com/">Copyscape</a>: Verifica a existência de páginas com conteúdo duplicado ao seu na web.</li>
  	  </ul>
      
    	<h2>White Hat SEO</h2>
        
    	<p>As White Hat SEOs seguem por via de regra as diretrizes dos mecanismos de busca para conseguir melhorar o seu posicionamento. Trabalham o conteúdo do site, se engajam em prática de troca de links que sejam coerentes com o seu conteúdo, não compram links com o simples propósito de subir no posicionamento do Google. Os resultados obtidos por sites white hat costumam demorar um pouco mais para aparecer, mas o risco de ser penalizado pelo Google e desaparecer dele é baixíssimo.</p>
        
    	<p>É no mínimo preocupante que um site com esses truques não tão difíceis de identificar tenha chegado ao primeiro lugar no Google. <strong><a href="http://seomarketing.com.br/contato-bh.html">Submeta a URL de um site Black Hat</a></strong> para que nós acompanhemos o seu desempenho nos Search Engines.</p>
        
