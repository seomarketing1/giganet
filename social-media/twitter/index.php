 <h2 style="font-size:16px;">O que é o Twitter?</h2>
 <p>É uma rede social do gênero microblogging, na qual seus usuários recebem e enviam atualizações de até 140 caracteres. Suas postagens podem ser realizadas pela internet, RSS, SMS ou por programas desenvolvidos para gerenciamento desta rede.</p>
 <h3>Twitter para empresas?</h3>
 <p>O perfil-empresa no Twitter é mais um ponto de contato com os consumidores. Com ele, os usuários terão acesso a novidades, curiosidades, além de poderem elogiar, reclamar e até solicitar um serviço por parte da empresa. O que difere de outras mídias é a velocidade, já que se trata de um meio extremamente dinâmico.</p>
 <h3>O que publicar?</h3>
 <p>Os assuntos a serem tratados nas redes sociais dependem da estratégia que a marca deseja seguir. É importante que todo o conteúdo possua alguma ligação com o nicho que a empresa faz parte, para que esse ponto de contato não seja descartado. No Twitter, essas postagens devem ser rápidas e informativas, com o objetivo de ganhar compartilhamento de seus usuários.</p>
 <h3>Como medir resultados?</h3>
 <p>É importante que a empresa veja e entenda como é sua situação nas redes sociais. O modo como ela é citada por seus usuários e fãs de redes sociais é uma boa forma de visualizar os próprios erros e assim melhorar o serviço oferecido aos seus consumidores. <br /> No Twitter, qualquer item de interação é importante. Nele podemos medir através de número de seguidores, RTs, marcações em listas e citações (do perfil ou apenas da marca).</p>
