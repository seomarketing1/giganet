 <h1>Consultoria SEO</h1>
 <h2>O que é a Consultoria SEO e como funciona?</h2>
 <p>Esta é uma dúvida recorrente entre muitos clientes que, em alguns casos, ocasiona na não contratação do serviço. Quem compraria ou contrataria um serviço que desconhece, não é verdade? Pensando nisso, decidimos criar esta página para ajudá-lo a entender as funcionalidades de uma Consultoria SEO. Apesar de cada caso ser diferente, a base a seguir permanece a mesma. Veja:</p>
 <h3>1ª Etapa - Assimilando-se ao Cliente</h3>
 <p>O cliente fornece informações básicas sobre o conteúdo; diz qual foco gostaria de seguir; mostra os dados de contas, como Google Analytics (caso já possua) e Google Webmaster Tools; além de informações sobre o servidor e tudo o que puder simplificar o nosso processo de análise.</p>
 <p>Durante esta etapa, é importante que haja uma reunião para que possamos nos conhecer melhor e estruturar o início da sua Campanha de SEO. Nesta reunião são definidos prazos, datas e diversos outros fatores. Tenha sempre em mente que manteremos um contato constante e que estamos sempre à disposição para ajudá-lo.</p>
 <h3>2ª Etapa - Análise SEO</h3>
 <p>Como o próprio nome sugere, nesta etapa nós iniciamos a criação de um Relatório SEO, o qual é dividido em quatro seções de análise: Keywords; On-Page; Análise Off-Page; e Concorrentes. Confira abaixo as definições:</p>
 <ul>
  <li><strong>Análise de Keywords</strong> - Observamos o cenário atual dos sites de busca, para que possamos definir quais serão as palavras-chave mais importantes a serem destacadas nas páginas. Durante a primeira seção, levamos em consideração todas as sugestões do cliente, para agregarmos as informações à nossa análise. Isto é fundamental para o sucesso da Consultoria SEO;</li>
  <li><strong>Análise On-Page</strong> - Representa a maior parte do Relatório SEO. Inclui a análise de títulos, descrição de páginas, estrutura HTML, peso das imagens, tempo de carregamento e performance como um todo, densidade das keywords no site, estrutura de links internos e externos, arquitetura do conteúdo, entre outros itens. Este trabalho é realizado em cada uma das páginas do seu site;</li>
  <li><strong>Análise Off-Page</strong> - Serve para estruturar um plano de Link Building. Analisamos os links que apontam para o seu site, qual a importância deles,  os textos utilizados nos links e ainda colhemos a maior quantidade de dados possíveis;</li>
  <li><strong>Análise de Concorrentes</strong> - Munidos de todo o conteúdo gerado durante as análises anteriores, realizamos comparações com seus principais concorrentes de SERP. Por mais know-how que uma agência possua, esta comparação sempre traz informações novas e valiosas sobre mercado e suas estratégias.</li>
 </ul>
 <h3>3ª Etapa - Implementação</h3>
 <p>Outra reunião é feita para mostrarmos ao cliente o que foi feito e decidido no relatório, onde cada ponto é explicado e exemplificado. A implementação pode ser feita de duas formas:</p>
 <ul>
  <li><strong>Implementação feita pelo cliente</strong> - É o responsável por aplicar cada um dos conceitos definidos. É uma ótima opção para clientes que possuem desenvolvedores próprios para o site, sendo que os custos da implementação não serão incluidos na proposta de trabalho da SEO Marketing para a Consultoria SEO;</li>
  <li><strong>Implementação feita pela SEO Marketing</strong> - A forma mais garantida de que tudo sairá conforme o planejado. Possuímos nossa equipe de desenvolvedores e analistas SEO para tomar conta deste processo. Esta opção é voltada ao cliente que não possui equipe própria de desenvolvimento.</li>
 </ul>
 <h3>4ª Etapa - Acompanhamento</h3>
 <p>Acompanhamos a implementação e os resultados de SEO, para que tudo ocorra de acordo com o planejado. O tempo de acompanhamento varia de cliente para cliente, seguindo o que foi definido no escopo inicial do projeto. Se estiver satisfeito com o nosso trabalho, você pode fazer como alguns de nossos clientes, que continuam com os serviços de Consultoria SEO, já que percebem a necessidade de se manterem atualizados no mercado digital.</p>
 <p>Agora que você conhece parte da nossa metodologia de trabalho, entre em contato conosco e solicite uma consultoria à sua empresa.</p>
