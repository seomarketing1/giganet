    	<h1><small>Tutorial Adwords</small><br/>Anúncios na Rede de Conteúdo</h1>
        
        <h2>Fazer a compra de uma palavra que tenha a ver com seu negócio aumentará a relevância do seu site na web</h2>
        
   	  <p>Quando se navega pela internet é cada vez mais comum encontrar propagandas em sites de notícias, blogs, portais, ou mesmo no seu webmail. Esta forma de publicidade pode aparecer em forma de links patrocinados, banners, imagens e mais recentemente vídeos. Para facilitar a vida de anunciantes e publishers, o Google trabalha com um conceito chamado de Rede de Conteúdo. A <strong>Rede de Conteúdo</strong> é composta de sites que "alugam" nas suas páginas espaços ao Google para que este veicule os anúncios de seus anunciantes.</p>
        
   	  <p>Nota: Recentemente, o Google passou a oferecer nos Estados Unidos a opção de veiculação de anúncios em rádio (Google Audio Ads) e jornais (Google Print Ads).</p>
        
   	  <h2>Que anúncios aparecerão na rede de conteúdo?</h2>
        
    	<p>O Google procura aplicar nos seus anúncios na rede de conteúdo o mesmo conceito de relevância que utiliza nos links patrocinados que aparecem na tela de resultados (SERP). Se um anunciante estiver "comprando" a palavra “espaço para festas”, o Google procurará veicular o seu anúncio em sites relacionados a esse assunto, em notícias de sites sobre crianças ou mesmo no Gmail (serviço de email do Google), quando o usuário ler algum e-mail relacionado a <a href="http://www.derhauseventos.com.br/" target="_blank">espaço para festas</a>.</p>
        
   	  <p>Assim como em links patrocinados, que aparecem na rede de pesquisa, anunciantes definem um preço máximo por clique. O Google usa esse preço para calcular a posição do anúncio na tela e se ele será exibido ou não.</p>
        
   	  <p>Confira o site do Google com a descrição de sites pertencentes à sua <a href="https://adwords.google.com/support/bin/answer.py?answer=6119&amp;ctx=awdash&amp;sourceid=awo&amp;subid=helplink&amp;hlrm=pt_BR" target="_blank">rede de conteúdo</a>.</p>
        
   	  <h2>Rede de conteúdo segmentada por sites</h2>
        
   	  <p>Anunciantes têm a opção de definir em quais sites da rede de conteúdo os seus anúncios aparecerão. Neste caso, o modelo de cobrança adotado é o CPM (Custo Por Mil impressões). Ao invés de pagar a cada clique, como acontece no modelo CPC (Custo por click), o anunciante define um valor para cada 1.000 vezes que seu anúncio for exibido nos sites de sua escolha.</p>
        
   	  <h2>Vantagens da rede de conteúdo</h2>
        
   	  <p>Anunciantes, usuários e donos de sites se beneficiam desse modelo, conforme os seguintes dados:</p>
        
   	  <p><strong>Anunciantes:</strong> conseguem veicular os anúncios em um grande número de sites, de uma interface unificada (Adwords), sem ter de negociar individualmente com diversos publishers. Eles conseguem monitorar o desempenho em real-time e podem rapidamente incluir novos sites ou desveicular anúncios de sites que não dão resultados. Anunciantes norte-americanos fazem uso da mesma interface para veicular anúncios em rádio (Google Audio Ads) e Jornais (Google Print Ads);</p>
        
   	  <p><strong>Usuários:</strong> visualizam propagandas relevantes aos temas que pesquisam e ganham uma nova fonte de pesquisa;</p>
        
   	  <p><strong>Blogueiros, portais e sites de notícias:</strong> passam a ter uma interface única para monetizar seus sites. Não precisam negociar individualmente com anunciantes e ganham um volume significativo, sem qualquer esforço adicional.</p>
        
