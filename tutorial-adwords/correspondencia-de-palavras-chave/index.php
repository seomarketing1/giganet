    	<h1><small>Tutorial Adwords</small><br/>Correspondência de Palavras-Chave</h1>
        
   	  <h2>Faça os seus anúncios aparecerem para o público-alvo correto</h2>
        
   	  <p><strong>Correspondência de palavras-chave</strong> - Quando você decide anunciar no Google, a primeira coisa a se preocupar é com a escolha das <strong>palavras-chave</strong>. Ao criar um grupo de anúncios, é necessário definir quais as palavras que farão parte desse grupo, isto é, aquelas que, quando digitadas por usuários do Google, fazem com que o seu anúncio apareça. O Google por definição mostra o anúncio para a palavra-chave digitada, plurais, sinônimos, erros gramaticais, complementos, etc. Se aquela que for comprada se referir à decoração, os usuários digitarão os seguintes termos na busca do Google que podem, eventualmente, gerar a visualização do anúncio:</p>
        
   	  <ul>
    	  <li>decoração</li>
    	  <li>decoracao</li>
    	  <li>decorações</li>
    	  <li>decração</li>
    	  <li>docoração de interiores</li>
    	  <li>decoração para festas infantis</li>
    	  <li>aula de decoração</li>
    	  <li>livros de decoração</li>
    	  <li>decoracao carnavalesca</li>
    	  <li>enfeites (** sinônimo **)</li>
  	  </ul>
      
   	  <p>Para evitar que o seu anúncio apareça para pesquisas fora do seu público-alvo, é necessário tomar alguns cuidados. Para isso, o Google oferece uma opção de correspondência de palavras-chave que permite focar os anúncios nas buscas que podem, de fato, atrair mais clientes ao site. Dominar com maestria essas correspondências pode economizar muito dinheiro em cliques desperdiçados e, muitas vezes, ser a diferença entre o sucesso e o fracasso do seu empreendimento na internet.</p>
        
   	  <h2>Correspondência ampla modificada</h2>
        
   	  <p>O <strong>modificador de correspondência ampla</strong> é um ótimo recurso de segmentação do Google AdWords. Ele permite criar palavras-chave com alcance maior a correspondência de frase e mais controle que o da correspondência ampla. Ao adicionar palavras-chave de correspondência ampla modificada a sua campanha, você poderá obter mais cliques e conversões por um ROI (Return on investment ou retorno do investimento) atraente, especialmente se usar palavras-chave de correspondência exata e de frase.</p>
        
    	<p>Para implementar o modificador, basta colocar um sinal de adição (+ sem espaços) diretamente à frente de uma ou mais palavras, em uma palavra-chave de correspondência ampla. Cada palavra precedida de + deve ser exibida em sua forma exata na pesquisa de seu cliente em potencial ou com uma variação aproximada. Entre as variações aproximadas estão os erros de ortografia, as formas singular/plural, as abreviações e os acrônimos, bem como as variações de um mesmo radical (como "casa" e "casinha"). Sinônimos (como "casa" e "moradia") e pesquisas relacionadas (como “flores” e “tulipas”) não são considerados como variações aproximadas.</p>
          
   	  <h2>Correspondência Ampla</h2>
        
   	  <p>A palavra "comprada" no Google Adwords dispara plurais, sinônimos, erros gramaticais, complementos, a melhor opção quando você não sabe exatamente que palavras anunciar. As palavras-chave devem ser digitadas sem qualquer caracter antes ou depois da palavra-chave. O exemplo descrito abaixo mostra possíveis termos que podem ser disparados pela palavra-chave decoração, quando incluída de forma ampla dentro do grupo:</p>
        
        <ul>
        	<li>decoração antiga;</li>
			<li>decorações;</li>
			<li>aprenda a decorar;</li>
			<li>projetos de decoração;</li>
			<li>decorando a música.</li>
		</ul>

   	  <h2>&quot;Correspondência de frase&quot;</h2>
        
   	  <p>A palavra-chave "comprada" no Google deve ser escrita entre aspas e só disparará anúncios para palavras-chave digitadas na busca, exatamente da forma que foram definidas e pode incluir palavras antes e depois daquela que foi comprada. Assim, se ela for "decoração de interiores", as seguintes buscas do Google podem fazer com que o seu anúncio seja exibido da seguinte forma:</p>
        
   	  <ul>
    	  <li>decoração de interiores</li>
    	  <li>exemplos de decoração de interiores</li>
    	  <li>fotos de decoração de interiores</li>
    	  <li>decoração de interiores rústicos</li>
      </ul>
      
   	  <p>As seguintes buscas por usuários no campo de buscas do Google <strong>NÃO</strong> farão com que o seu anúncio seja exibido:</p>
        
   	  <ul>
    	  <li>decoracao de interiores (** sem acentuação **)</li>
    	  <li>decração de interiors (** erro gramatical **)</li>
    	  <li>decoração interiores (** sem o de **)</li>
    	  <li>decoração de interior (** interiores no singular **)</li>
    	  <li>decoração e design de interiores (** design no meio da busca **)</li>
      </ul>
      
   	  <h2>[Correspondência exata]</h2>
        
    	<p>Apenas as buscas exatas no Google por meio de palavras digitadas como especificadas podem fazer com que o seu anúncio seja exibido. A palavra-chave deve ser digitada entre colchetes. Assim, a palavra-chave <em>[decoração de ambientes]</em> somente terá o seu anúncio exibido quando a busca no Google for <em>decoração de ambientes</em>. Qualquer busca com outra variação - sem acentuação, erros, sinônimos e complementos - não fará o anúncio ser exibido.</p>
        
   	  <h2>- Palavra-chave negativa</h2>
        
   	  <p>A palavra-chave negativa é a forma utilizada para inibir a aparição indesejada do anúncio para aquela de correspondência ampla e de frase. Ela deve ser digitada com o sinal de menos à sua frente. Assim, a palavra-chave negativa <em>(livro)</em>, utilizada no mesmo grupo de anúncios da palavra-chave de correspondência ampla <em>(decoração de interiores)</em> fará com que o anúncio apareça quando a busca no Google for feita para a palavra-chave fotos de decoraçao de interiores, mas não para buscas com a palavra-chave <em>"livro decoração de ambientes"</em>.</p>
        
