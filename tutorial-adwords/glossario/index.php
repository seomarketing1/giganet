   <div class="glossario_content"> <span class="glossario_content_title">Conversão</span> <span class="glossario_content_descritivo">Uma conversão é uma meta atingida em um site. Para um site de e-commerce, uma conversão é uma venda. Para sites de serviço, pode ser um pedido de orçamento. Para uma ONG, pode ser um e-mail de um futuro colaborador.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">CPA</span> <span class="glossario_content_descritivo">CPA (cost per action ou Custo por Ação) é a modalidade de pagamento por ação, ou seja, é o valor pago por um anunciante a cada ação, previamente acertada entre as partes, executada num site. Uma ação pode ser uma venda, um preenchimento de formulário, a visita a uma página ou qualquer outro objetivo pré-definido.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">CPC</span> <span class="glossario_content_descritivo">CPC (Custo por Click) é o valor pago por um anunciante em sites de busca para cada vez que um de seus anúncios é clicado.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">CPM</span> <span class="glossario_content_descritivo">CPM (Custo por Mil Impressões) é utilizado na modalidade de pagamento por impressões, ou seja,é o valor pago por um anunciamte por cada 1000 vezes que o seu anúncio é exibido, independente de ter sido clicado ou não.</span> </div>
