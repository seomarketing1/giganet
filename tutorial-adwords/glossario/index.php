   <div class="glossario_content"> <span class="glossario_content_title">Adsense</span> <span class="glossario_content_descritivo">Ferramenta do Google que permite a webmasters criarem em seus sites espaços para que o Google disponibilize anúncios de anunciantes que optem por anunciar em sua rede de pesquisa. É uma das formas mais fáceis e eficientes de monetizar sites que tenham um acesso grande de usuários.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">Adwords</span> <span class="glossario_content_descritivo">Ferramenta de links patrocinados do Google. Permite a anunciantes terem seus anúncios publicados na rede de pesquisa e na tela de resultados dos mecanismos de pesquisa.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">Affiliate Marketing</span> <span class="glossario_content_descritivo">Modalidade de publicidade aonde sites são comissionados por tráfego que levam até um site principal. Um site de detetives pode ter centenas de outros sites promovendo o seu serviço e sendo comissionados seja por tráfego direcionado ao site como por orçamento solicitado. Cada affiliate é identificado por um código exclusivo posicionado no link que aponta para o site principal.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">Arbitrage</span> <span class="glossario_content_descritivo">Alguns webdesigners monetizam seus sites comprando palavras-chave de custo baixo via Google Adwords e utilizando Google Adsense em seu site para disponibilizar anúncios de palavras mais caras. O método funciona se o total de receita com a Adsense compensar os gastos com Adwords. A esse processo chamamos Arbitrage.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">Behavioral Targeting</span> <span class="glossario_content_descritivo">Behavorial Targeting refere-se a um conceito aonde o comportamento histórico de pesquisas do usuário é utilizado na hora de exibir anúncios online, com o objetivo de aumentar a taxa de cliques e consequentemente a receita.</span> </div>
   <div class="glossario_content"> <span class="glossario_content_title">Cliques Inválidos</span> <span class="glossario_content_descritivo">Cliques inválidos se referem àqueles cliques em campanhas de links patrocinados que são desconsiderados pelos mecanismos de busca por terem origem duvidosa - robôs, scripts maliciosos ou concorrentes clicando no mesmo anúncio inúmeras vezes com o objetivo de diminuir o orçamento do site atacado.</span> </div>
