  <h2>SITE DE BUSCA, <br/>MOTOR DE BUSCA... </h2>
  <p>Imagine como seria a internet sem o Google, Yahoo, MSN ou outros sites similares. Chamados de sites de busca, mecanismos de busca, sistemas de busca, motores de busca, buscadores ou mecanismos de pesquisa, são ferramentas online que auxiliam usuários a buscar informações na internet. Ao contrário de diretórios controlados por editores humanos, sendo o DMOZ o mais importante, os principais sites de busca utilizam-se de algoritmos próprios para indexar a informação e disponibilizá-la aos usuários.
