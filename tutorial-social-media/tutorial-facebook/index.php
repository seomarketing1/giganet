   	  <h1><small>Tutorial Social Media</small><br/>Tutorial Facebook</h1>
        
      <h2>O Facebook</h2>

<p>Rede social lançada em 2004, criada por 5 colegas de faculdade, dentre eles seu atual CEO Mark Zuckerger e o brasileiro Eduardo Saverin. A rede tem hoje mais de 950 milhões de usuários no mundo, sendo que 60 milhões destes são do Brasil.</p>

<p>Nesse ambiente, cada pessoa pode ter seu próprio perfil para compartilhar gostos, fotos, vídeos. </p>

<p>Já as empresas, marcas e personalidades podem <a href="<?php echo $dominio; ?>social-media/fanpages-facebook-empresas/criacao-fanpages/">criar Fan Pages</a> para compartilhar conteúdo relevante, novidades, promoções.</p>

<p>Ao adicionar amigos, “curtir” empresas e marcas, o usuário recebe as atualizações deles em seus feeds. Dessa forma, ocorrem as interações entre usuários e empresas, por meio de comentários, “curtirs” e compartilhamentos nas postagens.</p>

<h2>Vantagens da Fanpage:</h2>

  <ul>
    <li>São indexadas pelos mecanismos de busca (Google, Bing...), o que aumenta o tráfego na página, e consequentemente no seu site;</li>
    <li>Visualmente se torna muito atrativo para o usuário, que em seu momento de lazer entra em página de empresas, buscando indicações de produtos e serviços;</li>
    <li>É possível criar campanhas de publicidade direcionadas para a página, dentro do Facebook;</li>
    <li>Não possui limitação de fãs (perfis estabelecem limites de amigos);</li> 
    <li>Sua empresa estará dentro dos <a href="http://www.facebook.com/legal/terms" target="_blank">termos de uso</a> do Facebook;</li>
    <li>A rede é a que mais cresce no mundo e a tendência é que seus usários passem a fazer compras através dela.</li>
  </ul>

<h2>Como conseguir Likes</h2>

<p>Nas redes, quem dita as regras é o consumidor e não o anunciante. Mas não se assuste, isso não é um problema, já que esse é o local em que podemos ter todas as informações que precisamos. A página de um usuário é repleta de informações relevantes. Nela, encontramos a localidade da pessoa, interesses e, em muitos casos, o consumidor diz com suas próprias palavras seu desejo, pela atualização de status. Basta saber onde procurar!</p>

<p>Seguindo esses passos, você passará a conhecer melhor seu público, podendo gerar o conteúdo que ele deseja receber em seu feed de atualizações, horários mais propícios para se fazer as postagens e o tipo de publicações que serão feitas (fotos, enquetes, links externos, ações promocionais que incentivem o usuário a interagir e compartilhar o conteúdo, e até a própria marca). Também é possível anunciar dentro da rede social, utilizando o Facebook Ads.</p>

<p>Como qualquer outra mídia social, o sucesso da Fan Page depende do engajamento dos usuários perante a marca ou empresa. Isso depende de um bom planejamento nas redes e na web como um todo, realizado com muita competência.</p>

<h2>O que é Facebook Marketing?</h2>

<p>São as estratégias de marketing aplicadas à presença de determinada empresa no Facebook para levar informações relevantes ao seu público-alvo, de forma que isso se reverta em resultados positivos de branding e vendas, de acordo com o objetivo e escopo de cada empresa.</p>

