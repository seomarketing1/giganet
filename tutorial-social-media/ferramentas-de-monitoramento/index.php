<h1><small>Tutorial Social Media</small><br/>Ferramentas de Monitoramento</h1>
        
<p>Quer saber o que os consumidores estão falando da sua marca? Quer conhecer seu público-alvo mais a fundo? E o seu concorrente, como está?</p>

<p>A internet é uma fonte rica de informações para ajudar a sua empresa a tomar decisões estratégicas importantes. Monitorar as redes sociais e o que está sendo dito no ambiente digital sobre a sua empresa e o seu concorrente é fundamental. </p>

<p>É possível identificar fraquezas e melhorá-las, identificar pontos fortes e trabalhá-los, identificar o perfil do consumidor e aprimorar ações.</p>

<p>Mas, de nada serve essa enxurrada de informações se elas não forem organizadas e analisadas. Por isso, utilizamos a plataforma líder de mercado em monitoramento online, o <strong>Scup</strong>.</p>

<p>A análise de sentimento permite identificar o volume de menções à sua marca que são positivas, negativas ou neutras. Cruzando a análise de sentimento com a classificação por tags, podemos saber, por exemplo, se um produto X está sendo aceito de forma positiva ou negativa, ou se certo aspecto de seu serviço está sendo elogiado ou criticado. Assim fica mais fácil e rápido identificar problemas e pontos fortes, para, a partir daí, tomar decisões mais acertivas para o seu negócio.</p>

<p>O monitoramento também é uma ferramenta aliada à estratégia de SAC e gerenciamento de crise, identificando rapidamente dúvidas e reclamações em diversos ambientes digitais. </p>

