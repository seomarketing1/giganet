<h1><small>Tutorial Social Media</small><br/>Tutorial Google+</h1>
        
<h2>O Google+</h2>
        
<p>É uma rede social lançada em 2011 pelo Google. Agrega diversos serviços do próprio Google e proporciona uma nova dinâmica entre usuários com os Círculos e Hangouts. Já possui mais de 250 milhões de usuários e é tido como a proposta do Google para concorrer com o Facebook.</p>

<h3>O botão +1</h3>

<p>O botão +1 é semelhante ao botão "Curtir" do Facebook. Mas, para utilizá-lo, basta ter um perfil no Google. </p>

<p>Ele permite recomendar páginas publicamente na web e compartilhar com seus círculos, caso você tenha perfil no Google+.</p>

<h3>Circulos</h3>

<p>Os Círculos permitem que você organize seus seguidores nas categorias desejadas, como por exemplo, “Amigos Pessoais”, “Colegas de Trabalho”, “Empresas”. Dessa forma, é possível segmentar o conteúdo que você compartilha.</p>

<p>Para as empresas é importante classificar os novos seguidores de sua página, segundo o seu perfil, idade, sexo, localização, etc. Assim, suas postagens podem ser exclusivas para certos círculos e aumentam a chance de interação. Para adicionar seus seguidores nos círculos, é preciso classificá-lo de forma exata e de forma manual (não tem como o seguidor se encaixar em um círculo, é você quem decide), sendo algo trabalhoso, mas que pode gerar bons resultados no futuro. Vale lembrar que você pode adicionar um mesmo usuário em mais de um círculo.</p>

<h3>Hangouts</h3>

<p>Opção para realizar uma vídeo-conferência gratuita com os seus usuários. Você cria o Hangout, define quais círculos vão ter acesso e depois é enviado um convite aos participantes perguntando se eles querem ou não assistir.</p>

<p>Outra opção são os Hangouts On Air, que permite que você faça um broadcast ao vivo para um número indeterminado de pessoas em todo o mundo. Depois de terminado o Hangout On Air, o vídeo é colocado automaticamente em sua conta verificada do YouTube.</p>

<p>É uma forte ferramenta para empresas que querem  gerar discussões e debates com formadores de opinião.</p>
 
<h3>Animações</h3>

<p>Diferentemente do Facebook, o Google+ aceita a postagem de gifs e animações.</p>

<h3>Conta verificada</h3>

<p>O Google+ já disponibilizou o selo de verificação de conta, entretanto, ainda não está liberado para as empresas. Por enquanto, o Google mesmo verifica a veracidade da conta, dando preferência para páginas de figuras públicas, celebridades e pessoas que forem adicionados ao grande número de círculos.</p>

<h3>Vários Administradores</h3>

<p>Após um longo tempo sem essa permissão, as páginas já podem ser administradas por até 50 pessoas. Basta o criador adicionar os e-mails nas configurações da página.</p>

