    	<h1><small>Tutorial SEO</small><br/>As penalidadades do Google</h1>
    	<h2>Confira aqui os diversos tipos de penalização que o Google pode emitir</h2>

   	  <p>Penalidades no Google é um dos temas mais polêmicos na área de SEO, até por não haver muitas referências oficiais do site com relação a elas. Elas se referem à perda de posicionamento ou total banimento do site na tela de resultados do Google. Existe muita boataria nos fóruns da internet a respeito dessas penalidades, por isso, use muito o bom senso em tudo o que ler a respeito. Muitas das supostas "advertências" podem, na verdade, ser atribuídas a alguma variação do algoritmo ou à perda de um link importante apontando para o site.</p>
        
   	  <p>Um site pode ser penalizado por não obedecer às diretrizes de qualidade do Google. As causas mais comuns são cloaking, texto e links ocultos, redirecionamentos mal-intencionados, conteúdo duplicado (geralmente copiado de outros sites) e conteúdo gerado dinamicamente.</p>
        
    	<p>Na maioria das vezes, o webmaster sabe os motivos da penalização, pois a maior parte dos truques foram feitos de maneira proposital. Há casos em que o webmaster recebeu a punição sem saber que infrigiam alguma das diretrizes do Google, por exemplo, ao utilizar diversos domínios não propriamente redirecionados.</p>
        
   	  <p>Veja abaixo algumas formas de penalização de um site.</p>
        
   	  <h3>Penalização Manual</h3>        
   	  <p>Um funcionário do Google examina o seu site e decide penalizá-lo por alguma violação de suas diretrizes de qualidade. Geralmente, esse modo de penalização ocorre quando algum usuário denuncia o site por meio da interface para webmaster do Google. Parece estar frequentemente relacionado aos fatores externos do site, como compra e troca de links.</p>
        
   	  <h3>Penalização Algorítmica</h3>        
   	  <p>Parece acontecer quando o Google identifica automaticamente, com o seu algoritmo, alguma falha grave no site, como por exemplo repetição excessiva de palavras (keyword stuffing). Geralmente, apenas um pequeno grupo de palavras-chave é penalizado.</p>
        
   	  <h2>Tipos de penalidades do Google</h2>
        
   	  <h3>-30 Penalty</h3>        
   	  <p>Perda de 30 posições no ranking. Se antes você aparecia em primeiro, passa a aparecer após o resultado de número 30, isto é, após a 4ª página do resultado de busca. Todos os seus posicionamentos caem em 30 posições, inclusive para a busca pelo nome de sua empresa.</p>
        
   	  <h3>-950 Penalty</h3>        
   	  <p>Perda de 950 posições no ranking do Google. Ao contrário da penalidade -30, esta parece afetar apenas um grupo de palavras-chave, não o site todo.</p>
    	
      <h3>Exclusão do Índice</h3>        
   	  <p>Essa penalidade não é boataria, pois o Google remove o índice dos sites que não estão de acordo com as suas diretrizes. O fato mais conhecido? BMW alemã. O site simplesmente desapareceu dos resultados do Google. Como identificar? Digite "site:(oseusite).com.br" na busca do Google. Se não retornar nenhum resultado, ou o site ainda não foi indexado pelo Google ou sofreu uma penalidade de exclusão.</p>
        
   	  <h3>Posição 6</h3>        
   	  <p>Sites de longa data caem da posição 1 ou 2 para a posição 6. Não existem muitos dados a respeito dessa penalidade, mas parecem estar relacionados ao Link Building. Matt Cutts, do Google, entrou na discussão e negou conhecer qualquer penalidade desse sentido.</p>
        
   	  <h2>Como se recuperar de uma penalidade do Google?</h2>
        
   	  <p>Acesse a interface para webmasters do Google e verifique se recebeu uma mensagem do Google informando a penalidade. Recorra a causa da penalização do Google por meio do pedido de reconsideração ou remoção da penalidade de posicionamento. É importante você concordar com o Google de que cometeu uma violação de suas diretrizes e afirmar que já removeu o problema.</p>
        
