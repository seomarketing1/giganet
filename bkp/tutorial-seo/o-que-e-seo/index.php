	  <h1><small>Tutorial SEO</small><br/>O que é SEO?</h1>
        
   	  <h2>Entenda o que é SEO e como é aplicado</h2>
        
   	  <p><strong>Sites de busca</strong>, também chamados de <strong>motores de busca</strong>, <strong>mecanismos de busca</strong>, <strong>mecanismos de pesquisa</strong> ou simplesmente <strong>buscadores</strong>, são hoje a principal fonte de procura de informações por todo o tipo de usuários da internet. A sua importância comercial torna-se evidente. Aparecer ou não aparecer no Google pode ser a diferença entre o sucesso ou fracasso de uma empresa. Isso é válido não apenas para empresas de e-commerce, que vendem produtos diretamente em seus sites na internet, mas para qualquer negócio onde os potenciais clientes utilizam-se da internet para buscar os seus fornecedores.</p>
        
   	  <p><strong>O que é SEO</strong>, então? SEO é um conjunto de técnicas que têm como principal objetivo tornar os sites mais amigáveis para os sites de busca, trabalhando palavras-chave selecionadas no conteúdo do site de forma que ele fique melhor posicionado nos resultados naturais.</p>
        
    	<h2>Quem faz SEO?</h2>
        
    	<p><strong>A própria empresa</strong>. Entendendo a importância estratégica de um trabalho de SEO, diversas empresas desenvolvem o conhecimento interno em suas áreas de TI para otimizar os seus sites. Esse processo deve envolver tanto a equipe de TI como as áreas que se beneficiam diretamente de um melhor posicionamento na <strong>SERP (Search Engine Results Pages)</strong>. A mensagem deve ser clara e direta, e um envolvimento de um maior número de pessoas e áreas resulta em mais conteúdo para alimentar a fome insaciável do Google. Um artigo do site <a href="http://searchengineland.com/070620-084055.php" target="_blank">Search Engine Land</a> dá os passos para se criar internamente a estrutura de SEO dentro de uma empresa.</p>
        
		<p><strong>Agências especializadas</strong>, SEMs. Muitas companhias não possuem profissionais qualificados ou recursos para tocar programas bem-sucedidos de SEO. Os algoritmos de mecanismos de busca estão em constante evolução e a maioria das empresas possuem outras prioridades que precisam ser atendidas, tornando mais difícil manter-se a par de todas as novidades no mundo SEO. Surgiram, então, agências especializadas em SEO que oferecem serviços de terceirização de SEO. Elas desenvolvem toda a estratégia de SEO ou tornam-se consultoras de empresas que desejam adquirir o conhecimento necessário para desenvolver a sua estratégia de <em>SEO in-house</em>.</p>
  
   	  <h2>Vantagens e desvantagens de cada modelo</h2>
        
   	  <p>Existem diversos pontos a se considerar antes de decidir entre os dois modelos (SEO <em>in-house</em> ou terceirizado). Uma estratégia <em>in-house</em> possui a vantagem de ser tocada por pessoas da própria empresa, que conhecem muito bem o negócio, os pontos fortes e fracos, concorrentes, etc. Por outro lado, é difícil para uma empresa ter o mesmo nível de conhecimento daquelas que são especializadas em SEO. Essas agências costumam ter acesso a informações atualizadas dos próprios buscadores que outras empresas geralmente não possuem.</p>
      
      <p>O investimento para se ter SEO <em>in-house</em> pode ser muito maior a contratar uma agência para desenvolver o trabalho. Seja qual for o modelo, é importante que exista na empresa que deseja ter o seu site otimizado uma pessoa que coordene todos os esforços para otimizar o seu site.</p>
      
