					<h2>&quot;Dicas para se destacar no Google&quot;</h2>

					<p>08/01/2009 17:17:11<br />

					  Por Editorial PC Magazine

				  </p>

					<p>Por Bernhard Schultze - SEO Marketing</p>

					<p>A cada dia, mais executivos e empreendedores do mundo online e offline se d&atilde;o conta da import&acirc;ncia de estarem bem posicionados nos resultados de sites de busca.  Para muitos, aparecer ou n&atilde;o na primeira p&aacute;gina do Google &eacute; a diferen&ccedil;a  entre o sucesso e fracasso de seu neg&oacute;cio. Pagar por cliques em campanhas de links patrocinados pode n&atilde;o ser eficiente ou economicamente vi&aacute;vel, restando ent&atilde;o aparecer nos resultados naturais, ou seja, os n&atilde;o pagos. Por uma limita&ccedil;&atilde;o matem&aacute;tica e para desespero de muitos, s&oacute; existe um primeiro lugar, e geralmente s&oacute; s&atilde;o exibidos 10 resultados na primeira p&aacute;gina. </p>

					<p>O Google utiliza perto de 200 vari&aacute;veis em constante muta&ccedil;&atilde;o para definir o posicionamento de cada site. Ao inv&eacute;s de tentar mape&aacute;-las, existem algumas regras b&aacute;sicas que podem ser seguidas.</p>

					<p><strong>1. Escolha as palavras-chave corretas</strong></p>

					<p>Voc&ecirc; quer aparecer na primeira p&aacute;gina do Google, mas j&aacute; parou para pensar para quais palavras? &Eacute; comum ouvir de clientes frases como &quot;quero aparecer em primeiro lugar para a palavra &lsquo;carros&rsquo;&quot;. Mas, convenhamos, quem digita &ldquo;carros&rdquo; no Google? Provavelmente uma crian&ccedil;a de cinco anos atr&aacute;s de fotos do Lightning McQueen, o carrinho vermelho e tagarela do filme da Disney. Aparecer em primeiro lugar para a palavra &ldquo;carros&rdquo; n&atilde;o significa que voc&ecirc; aparecer&aacute; em primeiro para &ldquo;perfume para carros&rdquo; ou &ldquo;classificados de autom&oacute;veis&rdquo;.  Um trabalho de SEO (do ingl&ecirc;s Search Engine Optimization, ou otimiza&ccedil;&atilde;o para sites de busca) come&ccedil;a exatamente na escolha das palavras-chave mais importantes para o seu neg&oacute;cio. Para cada palavra-chave escolhida, tenha no m&iacute;nimo uma p&aacute;gina em seu site relacionada a ela. &Eacute; importante ainda n&atilde;o se prender a termos t&eacute;cnicos demais que apenas voc&ecirc; e seu concorrente conhecem. Escolha as palavras mais popularmente utilizadas.

					</p>

					<p><strong>2. Conquiste o topo por m&eacute;rito</strong></p>

					<p>Sites de busca existem para oferecer a usu&aacute;rios os resultados mais relevantes poss&iacute;veis &agrave;s buscas efetuadas. Olhe para o seu site e pergunte-se: &quot;Eu mere&ccedil;o estar na primeira posi&ccedil;&atilde;o, na primeira p&aacute;gina dos resultados do Google? O meu site &eacute; o melhor? &Eacute; o que tem o conte&uacute;do mais relevante do meu setor? Sou a empresa com mais tempo de mercado, a mais conhecida?&quot;  Fa&ccedil;a-se merecedor da primeira p&aacute;gina do Google! Tenha um site rico em conte&uacute;do, com &aacute;reas &uacute;teis, educativas, com produtos exclusivos.

					</p>

					<p><strong>3. Mil palavras valem muito mais do que uma imagem...</strong></p>

					<p>Bem, ao menos para sites de busca... O Google l&ecirc; e interpreta o texto de seu site para determinar sobre o que este se trata e calcular em que posi&ccedil;&atilde;o deve aparecer para cada uma das palavras lidas. Um site muito gr&aacute;fico e com pouco texto pode ser excelente para transmitir um conceito diferenciado, mas n&atilde;o ajuda em nada ao Google decifrar o seu conte&uacute;do. Sites desenvolvidos em Flash ou Javascript tamb&eacute;m apresentam barreiras que podem impedir a sua correta interpreta&ccedil;&atilde;o. Procure desenvolver o seu site utilizando tecnologias que facilitem o trabalho do Google, como HTML, ASP, PHP, CSS e com p&aacute;ginas ricas em texto. </p>

					<p><strong>4. Seja o mais votado</strong></p>

					<p>O Google criou um mecanismo similar a uma elei&ccedil;&atilde;o chamado de PageRank, onde ao inv&eacute;s de votos, as p&aacute;ginas na internet que possuem uma maior quantidade de links apontados tornam-se mais propensas a se destacar nos resultados de busca. Cada link &eacute; considerado um voto de confian&ccedil;a dado pelo site. Al&eacute;m da quantidade de links, o Google avalia ainda a import&acirc;ncia e relev&acirc;ncia do site de origem do mesmo. Um link do site da PC Magazine, por exemplo, apontado para um site de jogos online tem muito mais peso do que 10 links de blogs sem import&acirc;ncia falando de turismo na Patag&ocirc;nia. &Agrave; medida em que o seu site for ficando mais conhecido, mais links espont&acirc;neos receber&aacute;. N&atilde;o custa acelerar o processo e pedir a seus principais parceiros comerciais e amigos para apontarem  um link para o seu site!</p>

