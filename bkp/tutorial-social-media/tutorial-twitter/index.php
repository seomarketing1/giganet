    	<h1><small>Tutorial Social Media</small><br/>Tutorial Twitter</h1>
        
        <h2>O Twitter</h2>

<p>Rede social lançada em 2006 por Jack Dorsey, também conhecida pela modalidade “microblogging”, onde seus usuários postam mensagens de até 140 caracteres conhecidos como Tweets. Os usuários são identificados por uma @ no início de seus usernames. É assim também que um usuário pode se comunicar diretamente com outro por meio de replys.</p>

<h2>Seguidores</h2>

<p>Diferente de outras redes sociais, no Twitter não é necessário que haja correspondência mútua entre usuários para que aconteça uma interação. É possível seguir alguém que não siga o seu perfil e vice-versa. Isso torna o Twitter uma poderosa mídia social de broadcasting.</p>

<p>Por conta dessa característica, ele é uma rede muito dinâmica e ágil, ou seja, com o aspecto temporal de uma conversa, tendo como recorde 4.064 tweets por segundo durante a exibição de uma final de futebol americano, o Super Bowl.</p>

<p>Não há diferença técnica entre perfis de pessoas e de empresas. A dinâmica é a mesma para todos. Ao seguir um perfil, você automaticamente começa a receber em sua “timeline” os tweets desse usuário.</p>

<h2>RT (Retweet)</h2>

<p>A sigla RT é a abreviação de Retweet. Quando um usuário dá RT em um tweet de outro usuário, ele replica o conteúdo dele para seus próprios seguidores. O Retweet pode ser dado clicando no botão "Retweet", ao passar o cursor do mouse por um tweet. Outra opção é copiar a mensagem a ser retweetada e postar em seu perfil colocando a sigla RT e o @usuário à frente do tweet.</p>

<h2>Hashtags</h2>

<p>O símbolo “#” é utilizado na rede social para identificar e agrupar em uma busca palavras-chave e assuntos importantes. Ao clicar em uma palavra com “#”, você é direcionado automaticamente a uma página que exibe todos os tweets que utilizaram a mesma palavra. Assim é possível acompanhar o que milhares de usuários da rede estão falando sobre um determinado assunto e, assim, disseminar conteúdo segmentado também.</p>

<h2>Trending Topics</h2>

<p>A rede social usa um algoritmo especial que identifica os assuntos mais falados durante todo o dia e mostra uma lista dos 10 mais. É possível acompanhar os assuntos mais falados no mundo inteiro ou determinar por localidade ou afinidade.</p>

<p>Ao clicar em um item que está nos Trending Topics (TT), você é direcionado a uma página que mostrará os tweets que estão sendo enviados sobre determinado assunto.</p>

