        <div id="glossario_soc_container">
        	<div class="glossario_content">
<span class="glossario_content_title">Avatar</span>
<span class="glossario_content_descritivo">imagem que identifica o perfil de um usuário nas mídas sociais. Pode ser utilizado qualquer tipo de imagem, como: desenhos e fotos.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Blogger</span>
<span class="glossario_content_descritivo">serviço Google, que oferece ferramentas para edição e gerenciamento de blogs, semelhantemente ao WordPress. O Blogger permite a hospedagem de um número ilimitado de blogs nos servidores do Google, que adotam o endereço .blogspot.com.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Facebook</span>
<span class="glossario_content_descritivo">rede social lançada em 2004. Fundada por Mark Zuckerberg, atualmente, possui mais de 500 milhões de usuários e com um valor estimado em US$ 56 bilhões.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Flickr</span>
<span class="glossario_content_descritivo">rede social voltada para hospedagem e compartilhamento de imagens. Oferece como ferramentas a criação de um perfil, de álbuns e adicionar amigos. Foi lançado em 2004 e recebe cerca de 3 mil fotos por minuto e em 2010 alcançou a marca de 5 bilhões de fotos publicadas.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Formspring</span>
<span class="glossario_content_descritivo">rede social baseada em perguntas e respostas. Tem sido utilizado também por empresas como FAQ, facilitando o atendimento ao cliente. Pode ser conectado com outras redes sociais, para que as perguntas respondidas sejam postadas automaticamente em redes como Twitter e Facebook. Foi criado em 2009.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Google Trends</span>
<span class="glossario_content_descritivo">ferramenta Google que apresenta o termos mais populares no momento. Nela é possível visualizar gráficos com a freqüência em que um termo particular é procurado em várias regiões do mundo, e em vários idiomas.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Google +</span>
<span class="glossario_content_descritivo">rede social lançada em 2011.Foi criada com o objetivo de agregar serviços sociais do Google, como Google Profiles, Google Buzz ePicasa Web. Também possui algumas características novas do mundo social como: círculos (grupos de amigos), sparks (sugestões de conteúdo) e hangouts (chat por vídeo).</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Hootsuite</span>
<span class="glossario_content_descritivo">plataforma para mídias sociais. Publica para sites como o Facebook, Twitter, LinkedIn, Foursquare, MySpace e WordPress, além de gerar gráficos de interação sobre cada post.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Instagram</span>
<span class="glossario_content_descritivo">aplicativo gratuito de compartilhamento de fotos para aparelhos Apple.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Klout</span>
<span class="glossario_content_descritivo">ferramenta para análise de mídias sociais. Mede a influência de um usuário através de sua rede social, através de dados coletados a partir de sites como Twitter e Facebook.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">LinkedIn</span>
<span class="glossario_content_descritivo">rede social voltada para a vida profissional. Possui mais de 85 milhões de membros e está presente em mais 200 países. Se trata da maior rede social focada unicamente em negócios e trabalhos.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Livestream</span>
<span class="glossario_content_descritivo">plataforma de streaming de vídeo que permite a seus usuários assistir e transmitir vídeos utilizando uma câmera e um computador através da internet. É responsável pelo serviço Twitcam que, vinculado ao Twitter, permite que usuários do microblog transmitam vídeos diretamente de sua conta, sem necessidade de cadastro no Livestream.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">MySpace</span>
<span class="glossario_content_descritivo">rede social lançada em 2003. Possui um sistema interno de emails e fórums. Atualmente é focada em perfis relacionados à música, como de cantores e bandas independentes.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Orkut</span>
<span class="glossario_content_descritivo">rede social filiada ao Google, criada em 2004 com o objetivo de ajudar seus membros a conhecer pessoas e manter relacionamentos. Em 2010 atingiu a marca de 85 milhões de usuários em todo o mundo e até o final de 2011 era a maior rede social no Brasil.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Pay With A Tweet</span>
<span class="glossario_content_descritivo">sistema de marketing de mídia social que permite que criadores de conteúdo concedam acesso ao seu trabalho (ebooks, músicas, etc.) em troca de um tweet ou um post no mural do Facebook.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Podcast</span>
<span class="glossario_content_descritivo">áudio digital (MP3 ou AAC) divulgado através de podcasting na internet e atualizado via RSS. Possui diversos formatos como programas de rádio, informativos, listas de música, etc.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Redes Sociais</span>
<span class="glossario_content_descritivo">Sob a perspectiva do conceito que engloba a mídia social, as redes sociais são um de seus elementos, os chamados sites de relacionamentos, como o Facebook e o Orkut. Já de um ponto de vista mais amplo, as redes sociais são uma estrutura social composta por pessoas ou organizações, conectadas por um ou vários tipos de relações, que partilham valores e objetivos comuns.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">ROI</span>
<span class="glossario_content_descritivo">"return on investment" (retorno sobre investimento) é a relação entre o dinheiro ganho ou perdido através de um investimento, e o montante de dinheiro investido.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Seeding</span>
<span class="glossario_content_descritivo">ato de espalhar um determinado assunto nas redes sociais. Estratégia para "semear" informações na web, distribuindo-as nas mídias sociais, redes de relacionamento e blogs. </span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">SEO</span>
<span class="glossario_content_descritivo">Conjunto de técnicas aplicadas na construção de páginas web de modo a garantir que estas sejam facilmente processadas por motores de busca.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Social CRM</span>
<span class="glossario_content_descritivo">ampliação do CRM praticado pela empresa, com utilização das mídias sociais para melhor conhecer o seu público e atingí-lo da melhor forma possíveis nestes canais sociais</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Sorteie.me</span>
<span class="glossario_content_descritivo">ferramenta online para a realização de sorteios nas mídias sociais.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Tag</span>
<span class="glossario_content_descritivo">termo classificatório para um determinado assunto. Com as tags a busca se torna mais rápida já que se é direcionado rapidamente a assuntos que se relacionam a ela.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Tumblr</span>
<span class="glossario_content_descritivo">plataforma blogging que permite aos usuários publicarem textos, imagens, vídeo, links, citações e áudio. Se trata de uma rede social que está em um nível intermediário entre o Blog e o Twitter. Usuários podem "seguir" outros usuários e ver seus posts em seu painel (dashboard). Também podem "gostar" (favoritar) ou "reblogar" (semelhante ao RT do Twitter) outros blogs.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Tweet Deck</span>
<span class="glossario_content_descritivo">ferramenta (semelhante ao HootSuite) que se trata de um navegador em tempo real, ligando-o com seus contatos às suas redes sociais.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Twitter Counter</span>
<span class="glossario_content_descritivo">ferramenta online que apresenta em gráficos a evolução do número de seguidores de seu perfil do Twitter. </span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">TwitPic</span>
<span class="glossario_content_descritivo">ferramenta online que permite aos usuários a postagem de fotos e vídeos no Twitter.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Twitter</span>
<span class="glossario_content_descritivo">O Twitter é um microblogging que permite aos seus usuários enviar e receber atualizações de outros utilizadores. Essas atualizações (twetts) possuem o limite de 140 caracteres. O Twitter foi criado em março de 2006 por Jack Dorsey e hoje possui mais de 145 milhões de usuários em todo o mundo.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">Wordpress</span>
<span class="glossario_content_descritivo">WordPress é um aplicativo de sistema de gerenciamento de conteúdo (cms) para web, escrito em PHP e executado em MySQL, especialmente para a criação de blogs.</span>
</div>

<div class="glossario_content">
<span class="glossario_content_title">YouTube</span>
<span class="glossario_content_descritivo">site de compartilhamento de vídeos em formato digital. Criado em 2005, logo foi adquirido pelo Google. Recebe 35 horas de vídeo por minuto e ultrapassou a marca de dois bilhões de visualizações por dia.</span>
</div>
