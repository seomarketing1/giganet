<h1><small>Tutorial Social Media</small><br/>Boas práticas em Social Media</h1>
        
<p>Criar um perfil pessoal para uma empresa no Facebook, pode causar uma série de aborrecimentos futuros, além de que, a qualquer momento, a rede social pode deletar o seu perfil. Por isso, lembre-se, ter uma Fan Page é a melhor opção.</p>

<p>Seguir os termos de uso das redes sociais é vital para ter um bom relacionamento com esses parceiros, para evitar transtornos nos perfis e na imagem da empresa. Afinal,  não seguir essas regras pode fazer com que a empresa perca a autorização de utilizar seu próprio perfil e pode até perdê-lo de vez.</p>

<p>É importante saber que o perfil da empresa não reflete o perfil pessoal do dono, por exemplo. Por isso, manter a constância de identidade e linguagem é essencial. Nada de um funcionário aparecendo com seu próprio perfil pessoal na página para difamar um consumidor que postou algo negativo. #fail</p>

<p>Postar com muita frequência pode afastar seus consumidores, mas postar com baixa frequência pode fazer você ficar esquecido. É fundamental conhecer seu públlico para encontrar o meio termo nesse aspecto e manter um bom relacionamento.</p>

<p>Falar só da sua empresa pode ser chato para os novos e antigos clientes. Uma empresa narcisista passa longe do que o consumidor espera de um perfil empresarial em redes sociais, afinal, ninguém gostaria de ter um amigo que só fala dele mesmo. Então, que tal postar sobre outros temas também? Falar sobre assuntos que estejam relacionados com o mercado de atuação da empresa é uma boa opção.</p>

