<h1><small>Tutorial Social Media</small><br/>Tutorial Foursquare</h1>
        
<h2>O Foursquare</h2>

<p>É uma rede social com serviço de geolocalização. Ele permite que você indique onde está por meio do aplicativo no seu dispositivo móvel. Criada em 2009 por Dennis Crowley, a rede possui mais de 10 milhões de usuários no mundo e tem hoje como foco a expansão no Brasil. </p>

<p><a href="http://blog.seomarketing.com.br/2012/09/solomo-o-novo-conceito-de.html" target="_blank">O SoLoMo (Social, Location-Based and Mobile)</a>, além de ser parte de um novo conceito de Social Media, é uma poderosa rede social para negócios físicos e locais que querem atingir seus consumidores no ambiente virtual. Dessa forma, ele torna possível surpreender de uma forma digital o consumidor no momento em que ele está em seu local físico.</p>

<h3>Venue</h3>

<p>É a representação virtual de um estabelecimento no Foursquare, ou seja, é o perfil que representará seu negócio físico na rede social. É neste local que o usuário poderá interagir com seu estabelecimento.</p>

<h3>Chek-In</h3>

<p>Para indicar o local em que o usuário se encontra, basta realizar um “Check-In”, ou seja, informar aos seus amigos que está em um determinado estabelecimento naquele momento. Também é possível compartilhar essa informação no Twitter e no Facebook.</p>

<h3>O Mayor</h3>

<p>É o usuário de maior prestígio em uma venue, por ser a pessoa que mais visitou o local em um determinado período de tempo. Normalmente, recebe regalias do estabelecimento por ser um consumidor fiel. </p>

<p>No entanto, basta que alguém faça mais check-ins do que o Mayor atual, para ele perder o título de prefeito para a outra pessoa. Isso cria um ambiente de competição saudável entre os usuários.</p>

<h2>Quais tipos de promoção posso fazer para meu negócio local com o Foursquare?</h2>

  <ul>
    <li>O Foursquare disponibiliza diversos formatos para que o negócio local possa se promover na rede social, todas são formas gratuitas, mas que devem ser muito bem administradas.</li>
    <li><strong>Friends Special:</strong> pode-se definir um número mínimo de amigos com os quais um usuário deve fazer check-in junto. O objetivo é trazer grupos de amigos à sua venue;</li>
    <li><strong>Swarm Special:</strong> define-se um intervalo de tempo e um número mínimo de pessoas fazendo check-ins para liberar a Special. Pode ser desbloqueada apenas uma vez ao dia; </li>
    <li><strong>Flash Special:</strong> define-se quantos Specials serão liberados para as primeiras pessoas que chegarem, a partir de determinado horário na venue;</li>
    <li><strong>Newbie Special:</strong> este tipo de Special é liberado quando um usuário dá check-in pela primeira vez na venue. É a forma mais direta de incentivar novas pessoas a frequentarem a venue;</li>
    <li><strong>Check-in Special:</strong> essa Special é liberada em qualquer check-in dado na venue, sob qualquer condição especificada pela empresa;</li>
    <li><strong>Mayor Special:</strong> esse Special é dado ao Mayor da sua venue. É uma ótima forma de premiar o seu melhor cliente e encorajar uma competição saudável;</li>
    <li><strong>Loyalty Special:</strong> há várias formas de usar o Loyalty. Pode-se liberá-la para um usuário que deu check-in em um determinado número de vezes, por números de visitas em um período pré-determinado, ou pelo número de visitas no total;</li>
    <li><strong>Tips:</strong> ao fazer o check-in em uma venue, o usuário pode deixar dicas e opiniões sobre o lugar que visitou.</li>
  </ul>
  
<p>Dessa forma, antes de visitar o local, os outros usuários saberão as opiniões de outras pessoas que já estiveram no estabelecimento.</p>

