    <h1>Tutorial Google Adwords</h1>
	
    <h2>Como funcionam os anúncios no Google</h2>
	
    <p>A cada termo procurado no Google, é oferecida ao usuário uma combinação de <strong>resultados naturais</strong> ou resultados orgânicos (1) e <strong>links patrocinados</strong> (2), também chamados de resultados pagos. Estes anúncios estão geralmente relacionados com a palavra-chave digitada pelo usuário ao realizar uma busca.</p>
	
    <img src="<?php echo $dominio;?>imagens/tutorial-adwords/serp-explained-sm.jpg" alt="SERP Google" />
    <p><em>Figura: SERP Google - Search Engine Results Page</em></p>
	  
    <h2>Estrutura do anúncio no Google</h2>
	
    <img src="<?php echo $dominio;?>imagens/tutorial-adwords/link-patrocinado.jpg" alt="Anúncio de Link Patrocinado" border="0" />
    <p><em>Figura: Estrutura do anúncio de texto</em></p>
	  
    <p>Uma procura por FESTAS INFANTIS pode por exemplo exibir o anúncio acima na área reservada para links patrocinados na tela de resultados do Google (SERP). O anúncio é composto de um título de até 25 caracteres, duas linhas de descrição com até 35 caracteres cada e um link com o endereço do anunciante. A palavra-chave digitada pelo usuário e que apareça no texto do anúncio fica destacada em negrito.</p>
	
    <h2>Que anúncios aparecerão na tela?</h2>
	
    <p>Empresas que desejam anunciar no Google devem registrar-se no Google Adwords, a ferramenta de administração de links patrocinados do Google. <em>Compram</em> então um conjunto de palavras-chave, escrevem o anúncio de texto (imagens e vídeos também são aceitos) e definem os valores a serem gastos na campanha de links patrocinados. Pronto!</p>
	
    <p>A cada vez que uma palavra-chave é digitada no campo de pesquisa do Google, este verifica todos os anunciantes que participam do leilão para os termos digitados. Por exemplo, uma mãe quer organizar a festa infantil de seu filho, e digita FESTAS INFANTISno campo de pesquisa do Google. Além de exibir 10 sites na área de resultados naturais, o Google verificará a sua base de anunciantes de links patrocinados e, se algum deles comprou uma das três combinações de palavras-chave - FESTAS, INFANTIS ou FESTAS INFANTIS, exibirá esses anúncios na tela de resultados na área reservada aos links patrocinados.</p>
	
    <p>Na verdade, anunciantes não <em>compram</em> palavras-chave. A cada vez que um usuário digita um termo, ocorre um <em>leilão virtual instantâneo</em> entre todos os anunciantes para aquelas palavras-chave. O anunciante só paga a cada vez que um de seus links patrocinados é clicado. A esse modelo de publicidade chamamos de <strong>PPC - Pay per click</strong>.</p>
	
    <h2>Que anúncios apareerão em <em>primeiro</em> na tela?</h2>
	
    <img src="<?php echo $dominio;?>imagens/tutorial-adwords/links-patrocinados.jpg" alt="Links Patrocinados - resultados" /><br />
    <p><em>Figura: Links Patrocinados</em></p>
	  
    <p>Uma vez selecionados os anunciantes que participarão do leilão da palavra-chave digitada, no nosso exemplo FESTA INFANTIL, o Google classificará os anúncios baseado em 2 fatores:</p>
	
    <ol>
      <li><strong>Preço máximo</strong> que cada anunciante está disposto a pagar por aquela palavra chave.</li>
      <li><strong>Índice de qualidade da palavra-chave</strong> de cada anunciante.</li>
    </ol>
	
    <h2>Posicionamento de um anúncio = Preço máximo X Índice de qualidade</h2>
	  
    <p>O <strong>índice de qualidade</strong> é um dos fatores que fez do Google o queridinho de Wall Street, a máquina de ganhar dinheiro em que se tornou. Passou de ser a empresa com o melhor mecanismo de busca para ser a empresa que disponibiliza os melhores, mais relevantes anúncios de links patrocinados. O índice de qualidade é um valor dado pelo a cada palavra-chave <em>comprada</em> pelo anunciante de links patrocinados, e baseia-se principalmente no seu desempenho histórico (quanto mais clicado melhor) e relevância com a palavra-chave pesquisada. O índice de qualidade é um dos fatores determinantes para definir se o anúncio aparecerá na posição 1 ou posição 2 da figura 6. Dito isto, o usuário do Google tem uma maior probabilidade de encontrar o que busca tanto nos resultados naturais como nos links patrocinados. E todos estes cálculos acontecem em uma fração de segundo!</p>
	
    <p>O Yahoo! recentemente alterou o seu algoritmo de disponibilização de anúncios de links patrocinados para um modelo bem parecido ao do Google, aonde a taxa de clique histórica do anúncio e a relevância são levados em consideração. Até meados de 2007, apenas o valor máximo pago por clique determinava o posicionamento do anúncio.</p>
	
